function encryptString(str) {
    let encrypted = '';
    for (let i = 0; i < str.length; i++) {
      
        let ascii = str.charCodeAt(i);
    
        ascii += 5;
        
        encrypted += String.fromCharCode(ascii);
    }
    return encrypted.substring(0, encrypted.length-2);
}

