function getRandomColor() {
    const minBrightness = 100;

    while (true) {
        var r = getRandomValue();
        var g = getRandomValue();
        var b = getRandomValue();

        const brightness = 0.3 * r + 0.59 * g + 0.114 * b;

        if (brightness >= minBrightness) {
            return `rgb(${r},${g},${b})`;
        }
    }
}
